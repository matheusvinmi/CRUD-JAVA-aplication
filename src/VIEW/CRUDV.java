    public void castrarUser(){
        String nome, email, senha;
        nome = Nome.getText();
        email = Email.getText();
        senha = Senha.getText();

        UsuarioDTO funDto = new UsuarioDTO();
        funDto.setNome_usuario(nome);
        funDto.setEmail_usuario(email);
        funDto.setSenha_usuario(senha);

        usuarioDAO funDao = new usuarioDAO();
        funDao.cadatrarUser(funDto);
    }
    
    
    public void listarTudo() {
        try {
            usuarioDAO userDAO = new usuarioDAO();
            DefaultTableModel Model = (DefaultTableModel) tabela.getModel();
            Model.setNumRows(0);

            ArrayList<UsuarioDTO> list = userDAO.ListarUsuario();
            for (int i = 0; i < list.size(); i++) {
                Model.addRow(new Object[]{
                    list.get(i).getId_usuario(),
                    list.get(i).getNome_usuario(),
                    list.get(i).getEmail_usuario(),
                    list.get(i).getSenha_usuario()
                });

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "erro em listarTudo" + e);
        }
    }

    public void carregarCampo() {
        int pegarL = tabela.getSelectedRow();

        cmpId.setText(tabela.getModel().getValueAt(pegarL, 0).toString());
        Nome.setText(tabela.getModel().getValueAt(pegarL, 1).toString());
        Email.setText(tabela.getModel().getValueAt(pegarL, 2).toString());
        Senha.setText(tabela.getModel().getValueAt(pegarL, 3).toString());
    }

    public void limparCampos() {
        cmpId.setText("");
        Nome.setText("");
        Email.setText("");
        Senha.setText("");
    }

    public void alterarUser() {
        usuarioDAO userDAO = new usuarioDAO();
        UsuarioDTO userDTO = new UsuarioDTO();
        int id = Integer.parseInt(cmpId.getText());
        String nome = Nome.getText();
        String email = Email.getText();
        String senha = Senha.getText();

        userDTO.setId_usuario(id);
        userDTO.setNome_usuario(nome);
        userDTO.setEmail_usuario(email);
        userDTO.setSenha_usuario(senha);

        userDAO.alterarUser(userDTO);

    }

    public void excluirUser() {
        usuarioDAO userDAO = new usuarioDAO();
        UsuarioDTO userDTO = new UsuarioDTO();

        int id = Integer.parseInt(cmpId.getText());
        userDTO.setId_usuario(id);

        userDAO.excluirUser(userDTO);
    }
